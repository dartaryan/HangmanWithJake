def choose_word(file_path, index):
    realindex = index
    splitwords = []
    bringwords = open(file_path, "r")
    words = (bringwords.read()).split(" ")
    for i in range(len(words)):
        if words[i] not in splitwords:
            splitwords.append(words[i])
    countwords = len(splitwords)
    countforchoose = len(words)
    while realindex > countforchoose:
        realindex = realindex - countforchoose
    chosenword = words[realindex - 1]
    return (chosenword)


