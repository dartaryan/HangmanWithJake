def show_hidden_word(secret_word, old_letters_guessed):
    i = 0
    theguess = ""
    while i < len(secret_word):
        if secret_word[i] in old_letters_guessed:
            theguess = theguess + secret_word[i]
        else:
            theguess = theguess + "_"
        i += 1
    return theguess


